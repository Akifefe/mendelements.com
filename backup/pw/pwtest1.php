<h2><a href="http://politicalwire.com/archives/2013/06/21/republicans_have_already_lost_the_hispanic_vote.html" title="Republicans Have Already Lost the Hispanic Vote">Republicans Have Already Lost the Hispanic Vote</a></h2>


<a href="http://www.nationaljournal.com//columns/cook-report/republicans-face-a-choice-expand-or-expire-20130620">Charlie Cook</a>: "As the immigration issue moves front and center in Congress, a chorus of GOP voices is warning that if immigrants--and, let's face it, the debate is focused mostly on those from Mexico, Central America, and South America--are provided a path to citizenship, Republicans will never again win a national election. The same people also argue that Republicans would find it much harder to win statewide races in places such as Texas that now routinely fall into the GOP column. Implicit in both arguments is the notion that Republicans will remain a competitive party nationally and stay dominant in certain states if they can prevent Hispanic immigrants from becoming citizens and gaining voting rights."<br /><br />

"But that position ignores the fact that the 45.5 million Hispanics already in this country legally are registering to vote and are seeing the Republican Party as distinctly hostile. The more the GOP comes to be seen as fighting immigration reform, the more difficult it will be for Republican candidates to compete for this group of voters. Remember, 50,000 Hispanic citizens reach voting age every month."




